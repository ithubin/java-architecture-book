 SELECT P.*
        FROM T_SYS_USER U
        LEFT JOIN T_SYS_USER_ROLE SUR ON U.ID= SUR.SYS_USER_ID
        LEFT JOIN T_SYS_ROLE R ON SUR.SYS_ROLE_ID=R.ID
        LEFT JOIN T_SYS_ROLE_PERMISSION SRP ON SRP.ROLE_ID=R.ID
        LEFT JOIN T_SYS_PERMISSION P ON P.ID =SRP.PERMISSION_ID
        WHERE U.ID=1
		
		
DROP TABLE PUBLIC.T_SYS_PERMISSION;		
CREATE TABLE PUBLIC.T_SYS_PERMISSION
(
    ID INTEGER,
    NAME CHARACTER VARYING(64) COLLATE PG_CATALOG."DEFAULT",
    URL CHARACTER VARYING(128) COLLATE PG_CATALOG."DEFAULT",
    METHOD CHARACTER VARYING(64) COLLATE PG_CATALOG."DEFAULT"
)

DROP TABLE PUBLIC.T_SYS_ROLE;
CREATE TABLE PUBLIC.T_SYS_ROLE
(
    ID INTEGER,
    NAME CHARACTER VARYING(64) COLLATE PG_CATALOG."DEFAULT"
)

DROP TABLE PUBLIC.T_SYS_ROLE_PERMISSION;
CREATE TABLE PUBLIC.T_SYS_ROLE_PERMISSION
(
    ID INTEGER,
    ROLE_ID INTEGER,
    PERMISSION_ID INTEGER
)

DROP TABLE PUBLIC.T_SYS_USER;
CREATE TABLE PUBLIC.T_SYS_USER
(
    ID INTEGER,
    USERNAME CHARACTER VARYING(64) COLLATE PG_CATALOG."DEFAULT",
    PASSWORD CHARACTER VARYING(64) COLLATE PG_CATALOG."DEFAULT"
)

DROP TABLE PUBLIC.T_SYS_USER_ROLE;
CREATE TABLE PUBLIC.T_SYS_USER_ROLE
(
    SYS_USER_ID INTEGER,
    SYS_ROLE_ID INTEGER
)
		
